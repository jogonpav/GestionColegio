    public v_Mostrar_Contenidos_Cmatriculados() {
        initComponents();
        conexion cn = new conexion();
        int con = 0;
        try {
            Connection conn = cn.conectar();
            Statement stmt = conn.createStatement();
            String query = "SELECT titulo, descripcion FROM contenidos WHERE curso_id = "+id_curso_seleccionado;
            ResultSet rs = stmt.executeQuery(query);
            while (rs.next()){
                titulos.add(rs.getString(1).toUpperCase());
                descripcion.add(rs.getString(2));
                con++;
            }
            if (con>0){
                for (int i = 0; i < titulos.size(); i++) {
                    todo_contenido = todo_contenido + (i+1)+". "+titulos.get(i)+"\n"+"     "+descripcion.get(i)+"\n\n";
                }
            }else{
                todo_contenido = "El curso aÃºn no tiene contenidos";        
            }
            jTextArea1.setText(todo_contenido);
        }catch(SQLException ex){
            System.out.println(ex);}
        
    }

