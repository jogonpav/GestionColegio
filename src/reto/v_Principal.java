    public void consultaDB (){ //METODO PARA VALIDAR EL USUARIO EN LA VASE DE DATS
         user.setTipo("Invalido"); //usuario que invalida el ingreso
         user.setUser(jTextField1.getText());
         conexion cn = new conexion(); 
         try {
             Connection conn = cn.conectar();  
             Statement stmt=conn.createStatement();  
             String query= "select * from usuarios"; //Consulta los campos id, user, contrasena y tipo de la tabla usuarios
             ResultSet rs = stmt.executeQuery(query);
             while(rs.next()){            
                 if (user.getUser().equals(rs.getString(2))){  //comparar user
                     char [] passdDB = rs.getString(3).toCharArray();    //METODO DE VALIDACIÓN DE JPASSWORDFIELD, es necesario pasar los datos a un array de tipo char    
                     char[] comparepasswordfield = jPasswordField1.getPassword();
                     if (passdDB.length == comparepasswordfield.length){
                         if (Arrays.equals (passdDB, comparepasswordfield)){     
                             user.setTipo(rs.getString(4));
                             user.setUsuarios_id(rs.getInt(1));
                             Arrays.fill(passdDB, '0'); //Cerear la posible contraseña por seguridad
                             Arrays.fill(comparepasswordfield, '0'); //Cerear la posible contraseña por seguridad
                         break;
                         }
                     }
                 }
             }   
         conn.close();
         }    
        catch(Exception e){ System.out.println(e);}
    }

