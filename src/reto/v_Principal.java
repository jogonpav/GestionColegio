    private void guardarAcceso() { //FUNCION PARA GARDAR ACCESO
        user.setFecha_ingreso(java.sql.Date.valueOf(LocalDate.now())); //guardar fecha tipo sql
        DateTimeFormatter F = DateTimeFormatter.ofPattern("hh:mm:ss");
        user.setHora_ingreso(LocalTime.parse(LocalTime.now().format(F)));
        conexion cn = new conexion(); 
        try {
            Connection conn = cn.conectar();  
            Statement stmt=conn.createStatement();  
            String query= "INSERT INTO accesos (usuario,estado, fecha_ingreso, hora_ingreso) VALUES ('"+user.getUser()+"','"+user.getTipo()+"','"+user.getFecha_ingreso()+"','"+user.getHora_ingreso()+"')";
            stmt.executeUpdate(query);
        conn.close();
        }    
       catch(Exception e){ System.out.println(e);}
    }
